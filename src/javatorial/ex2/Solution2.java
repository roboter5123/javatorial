package javatorial.ex2;

public class Solution2 {

	// Use a variable to print some text
	// Then reassign a different text to the variable and print it again
	public static void main(String[] args) {
		String helloWorld = "Hello Java World!";
		System.out.println(helloWorld);
		helloWorld = "Goodby Java World!";
		System.out.println(helloWorld);
	}
}
